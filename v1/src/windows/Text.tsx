import WindowWrapper from "../hoc/WindowWrapper.tsx";
import useWindowStore from "../store/window.tsx";
import { WindowControls } from "../components";
import type { FileDataType } from "../types";

const Text = () => {
  const { windows } = useWindowStore();
  const data = (windows.txt_file?.data ?? null) as FileDataType | null;

  if (!data) {
    return null;
  }

  const { name, imageUrl, subtitle, description } = data;

  return (
    <>
      <div id={"window-header"}>
        <WindowControls target={"txt_file"} />
        <h2>{name}</h2>
      </div>

      <div className={"p-5 space-y-6 bg-white"}>
        {imageUrl ? (
          <div className={"w-full"}>
            <img
              src={imageUrl}
              alt={name}
              className={"w-full h-auto object-cover rounded"}
            />
          </div>
        ) : null}

        {subtitle ? (
          <h3 className={"text-lg font-semibold"}>{subtitle}</h3>
        ) : null}

        {Array.isArray(description) && description.length > 0 ? (
          <div className={"space-y-3 leading-relaxed text-base text-gray-800"}>
            {description.map((para, idx) => (
              <p key={idx}>{para}</p>
            ))}
          </div>
        ) : null}
      </div>
    </>
  );
};

const TextWindow = WindowWrapper(Text, "txt_file");

export default TextWindow;